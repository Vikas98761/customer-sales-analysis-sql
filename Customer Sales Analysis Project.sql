--1.Find total revenue generated by the company--

SELECT SUM(p.price * s.quantity) AS total_revenue
FROM Sales s
JOIN Products p ON s.product_id = p.product_id;

--2. Customer Segmentation (High / Medium / Low)--

SELECT c.customer_name,
       SUM(p.price * s.quantity) AS total_spent,
       CASE
           WHEN SUM(p.price * s.quantity) >= 60000 THEN 'High'
           WHEN SUM(p.price * s.quantity) BETWEEN 20000 AND 59999 THEN 'Medium'
           ELSE 'Low'
       END AS customer_segment
FROM Sales s
JOIN Customers c ON s.customer_id = c.customer_id
JOIN Products p ON s.product_id = p.product_id
GROUP BY c.customer_name;

--3. Top Selling Product--

SELECT TOP 1 p.product_name,
       SUM(s.quantity) AS total_sold
FROM Sales s
JOIN Products p ON s.product_id = p.product_id
GROUP BY p.product_name
ORDER BY total_sold DESC;

--4. City-wise Sales Analysis--

SELECT c.city,
       SUM(p.price * s.quantity) AS city_revenue
FROM Sales s
JOIN Customers c ON s.customer_id = c.customer_id
JOIN Products p ON s.product_id = p.product_id
GROUP BY c.city
ORDER BY city_revenue DESC;

--5. Monthly Growth Analysis--

SELECT FORMAT(s.sale_date, 'yyyy-MM') AS month,
       SUM(p.price * s.quantity) AS monthly_revenue
FROM Sales s
JOIN Products p ON s.product_id = p.product_id
GROUP BY FORMAT(s.sale_date, 'yyyy-MM')
ORDER BY month;

--Bonus: Monthly Growth % using LAG()

SELECT month,
       monthly_revenue,
       LAG(monthly_revenue) OVER (ORDER BY month) AS prev_month,
       (monthly_revenue - LAG(monthly_revenue) OVER (ORDER BY month)) * 100.0
       / LAG(monthly_revenue) OVER (ORDER BY month) AS growth_percent
FROM (
    SELECT FORMAT(s.sale_date, 'yyyy-MM') AS month,
           SUM(p.price * s.quantity) AS monthly_revenue
    FROM Sales s
